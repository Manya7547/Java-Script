let month = "Mar";

switch(month){
    case "Jan":
        console.log("January");
        break;
    case "Feb":
        console.log("February");
        break;
    case "Mar":
        console.log("March");
        break;
    case "Apr":
        console.log("April");
        break;
    case "May":
        console.log("May");
        break;
    case "June":
        console.log("June");
        break;
    case "July":
        console.log("July");
        break;
    case "Aug":
        console.log("August");
        break;
    case "Sept":
        console.log("September");
        break;
    case "Oct":
        console.log("October");
        break;
    case "Nov":
        console.log("November");
        break;
    case "Dec":
        console.log("December");
        break;
    default: 
        console.log("Incorrect month");
        break;
}